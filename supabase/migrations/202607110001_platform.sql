create table if not exists public.sources (
  id bigint generated by default as identity primary key,
  name text not null, slug text not null unique, source_type text not null
    check (source_type in ('RSS','HTML','JSON_API','OFFICIAL_PDF_LIST','SITEMAP','MANUAL')),
  parser_type text not null, base_url text not null, feed_url text,
  official boolean not null default false, discovery_only boolean not null default true,
  enabled boolean not null default false, categories text[] not null default '{}', state text,
  authority_type text, allowed_domains text[] not null default '{}',
  allowed_document_domains text[] not null default '{}', item_selector text, title_selector text,
  link_selector text, summary_selector text, date_selector text,
  min_interval_minutes integer not null default 120 check (min_interval_minutes >= 0),
  request_timeout integer not null default 20 check (request_timeout between 1 and 60),
  max_items integer not null default 20 check (max_items between 1 and 100),
  robots_status text, terms_reviewed boolean not null default false, selector_verified_at timestamptz,
  last_success_at timestamptz, last_failure_at timestamptz,
  consecutive_failures integer not null default 0, health_status text not null default 'UNKNOWN',
  notes text, created_at timestamptz not null default now(), updated_at timestamptz not null default now(),
  check (not enabled or source_type in ('RSS','JSON_API','SITEMAP','MANUAL')
         or (item_selector is not null and title_selector is not null and link_selector is not null))
);

create table if not exists public.notices (
  id bigint generated by default as identity primary key,
  category text not null, subtype text not null default 'NEW', original_title text not null,
  title_bn text, title_en text, issuing_authority text, discovery_source_id bigint references public.sources(id),
  discovery_url text not null unique, source_name text, source_domain text, discovery_summary text,
  candidate_official_links jsonb not null default '[]', source_official boolean not null default false,
  discovery_only boolean not null default true, official_page_url text, official_document_url text,
  final_resolved_url text, canonical_official_url text, official_domain text,
  official_document_hash text, notice_number text, notice_date text, published_date text, deadline date,
  original_deadline_text text, deadline_state text not null default 'UNKNOWN'
    check (deadline_state in ('OPEN','CLOSING_SOON','EXPIRED','CANCELLED','UNKNOWN')),
  eligibility_status text, west_bengal_relevance text not null default 'LOW'
    check (west_bengal_relevance in ('HIGH','MEDIUM','LOW','REJECT')),
  relevance_reason text, publication_priority text not null default 'NORMAL'
    check (publication_priority in ('URGENT','HIGH','NORMAL','DIGEST_ONLY','REJECT')),
  publication_status text not null default 'DRAFT'
    check (publication_status in ('DRAFT','REVIEW','APPROVED','PUBLISHED','REJECTED')),
  verification_score integer not null default 0 check (verification_score between 0 and 100),
  verification_status text not null default 'DISCOVERED', structured_data jsonb,
  conflict_reason text, render_status text, revision_number integer not null default 1,
  manually_approved boolean not null default false, manual_override_reason text,
  first_seen_at timestamptz not null default now(), last_checked_at timestamptz not null default now(),
  verified_at timestamptz, posted_at timestamptz, created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create unique index if not exists notices_official_revision_unique
  on public.notices(canonical_official_url, official_document_hash)
  where canonical_official_url is not null and official_document_hash is not null;
create index if not exists notices_category_status_idx on public.notices(category, publication_status);
create index if not exists notices_deadline_idx on public.notices(deadline, deadline_state);
create index if not exists notices_relevance_idx on public.notices(west_bengal_relevance);
create index if not exists notices_canonical_url_idx on public.notices(canonical_official_url);

create table if not exists public.notice_revisions (
  id bigint generated by default as identity primary key, notice_id bigint not null references public.notices(id) on delete cascade,
  revision_number integer not null, official_document_hash text not null, official_url text,
  subtype text not null default 'UPDATED', structured_data jsonb, change_summary text,
  detected_at timestamptz not null default now(), unique(notice_id, revision_number)
);
create index if not exists notice_revisions_notice_idx on public.notice_revisions(notice_id, detected_at desc);

create table if not exists public.notice_evidence (
  id bigint generated by default as identity primary key, notice_id bigint not null references public.notices(id) on delete cascade,
  field_name text not null, extracted_value jsonb, evidence_text text, page_number integer check (page_number > 0),
  source_url text not null, validation_status text not null default 'PENDING', created_at timestamptz not null default now()
