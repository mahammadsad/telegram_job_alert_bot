drop policy if exists "public published notices" on public.notices;
create policy "public published notices" on public.notices for select to anon, authenticated
  using (publication_status='PUBLISHED' and verification_status in ('VERIFIED_OFFICIAL','POSTED'));
drop policy if exists "public published revisions" on public.notice_revisions;
create policy "public published revisions" on public.notice_revisions for select to anon, authenticated
  using (exists(select 1 from public.notices n where n.id=notice_id and n.publication_status='PUBLISHED'
               and n.verification_status in ('VERIFIED_OFFICIAL','POSTED')));
drop policy if exists "public published evidence" on public.notice_evidence;
create policy "public published evidence" on public.notice_evidence for select to anon, authenticated
  using (validation_status='VALID' and exists(select 1 from public.notices n where n.id=notice_id
         and n.publication_status='PUBLISHED' and n.verification_status in ('VERIFIED_OFFICIAL','POSTED')));

