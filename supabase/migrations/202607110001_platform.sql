create or replace function public.is_admin() returns boolean language sql stable security definer
set search_path = public as $$
  select exists(select 1 from public.profiles where id = auth.uid() and role in ('admin','reviewer'));
$$;

create or replace function public.increment_provider_usage(p_provider text, p_operation text)
returns integer language plpgsql security definer set search_path = public as $$
declare result integer;
begin
  insert into public.provider_usage(provider, usage_date, operation, calls)
  values (p_provider, current_date, p_operation, 1)
  on conflict(provider, usage_date, operation) do update set calls=provider_usage.calls+1
  returning calls into result;
  return result;
end $$;
revoke all on function public.increment_provider_usage(text,text) from public, anon, authenticated;
grant execute on function public.increment_provider_usage(text,text) to service_role;

